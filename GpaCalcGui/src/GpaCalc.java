    private void btnCalcGPAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcGPAMouseClicked
    //the event for mouse click for the calcgpa button
        
    DecimalFormat dfGPA = new DecimalFormat("0.00"); //setting the format for the object method to format the gpa.
    
    double dIS100Cred = Double.parseDouble(txtIS100.getText()); //i set the variables to make adding the total credits easier.
    double dIS102Cred = Double.parseDouble(txtIS102.getText());// i used the parse double to change the string from the txt fields
    double dIS201Cred = Double.parseDouble(txtIS201.getText());// where the number of credits are put in to make sure to be
    double dIS303Cred = Double.parseDouble(txtIS303.getText());//able to use math lower down when adding them together.
    double dACC200Cred = Double.parseDouble(txtACC200.getText());//i made sure to call the parsedouble method, then the get text
    double dFIN201Cred = Double.parseDouble(txtFIN201.getText());//method to make sure i grabbed the correct value from each.
    double dBUSM241Cred = Double.parseDouble(txtBUSM241.getText());
    
    double dTotalCredits =  dIS100Cred + dIS102Cred + dIS201Cred + dIS303Cred + dACC200Cred + dFIN201Cred + dBUSM241Cred; //adding all the variable together for the total number of credits.
    
    double dIS100GPA = CalcClassGPA(Double.parseDouble(txtIS100.getText()), String.valueOf(cboxIS100.getSelectedItem())); //get class weighted avg for each class.
    double dIS102GPA = CalcClassGPA(Double.parseDouble(txtIS102.getText()), String.valueOf(cboxIS102.getSelectedItem()));// this is calling the method i made previously (CalcClassGPA()) to get each
    double dIS201GPA = CalcClassGPA(Double.parseDouble(txtIS201.getText()), String.valueOf(cboxIS201.getSelectedItem()));//classes waited gpa. it made it easier creating one method and referencing
    double dIS303GPA = CalcClassGPA(Double.parseDouble(txtIS303.getText()), String.valueOf(cboxIS303.getSelectedItem()));//to the method to save code and space.
    double dACC200GPA = CalcClassGPA(Double.parseDouble(txtACC200.getText()), String.valueOf(cboxACC200.getSelectedItem()));//the string.valueOf gets the string originally in the txt fields and converts
    double dFIN201GPA = CalcClassGPA(Double.parseDouble(txtFIN201.getText()), String.valueOf(cboxFIN201.getSelectedItem()));//them into a string. thanks java for that code.
    double dBUSM241GPA = CalcClassGPA(Double.parseDouble(txtBUSM241.getText()), String.valueOf(cboxBUSM241.getSelectedItem()));// gave varaible to easily add up the total weighted gpa below
    
    double dTotalWeightGPA = dIS100GPA + dIS102GPA + dIS201GPA + dIS303GPA + dACC200GPA + dFIN201GPA + dBUSM241GPA; //getting the total number of gpas to soon calculate the avg.
    
    double dGPA = dTotalWeightGPA / dTotalCredits; //getting the average gpa all together using the number of total number of gpa divided by the credits for weight.
      
    txtGPA.setText(String.valueOf(dfGPA.format(dGPA))); //calling the object and its class to format the gpa. and then setting the result as the diplay for the txt box meant to display gpa.
    
    if(dGPA >= 3.7) //this is to choose whichever colored panel should appear.
    {
        pnlGreen.setVisible(true);//true allows the panal to be visible
        pnlYellow.setVisible(false); //while false makes sure its invisible.
        pnlRed.setVisible(false);//initially i made each panal = .setVisible(false); in the custom code menu.       
    }
    else if (dGPA >= 3.2) //if its lower than 3.7 but higher than 3.2 i made sure that yellow popped up.
    {
        pnlGreen.setVisible(false);
        pnlYellow.setVisible(true);
        pnlRed.setVisible(false);
    }
    else //and if the other two condidtions werent fulfilled i set it to make red appear.
    {
        pnlGreen.setVisible(false);
        pnlYellow.setVisible(false);
        pnlRed.setVisible(true);
    }
    
    }//GEN-LAST:event_btnCalcGPAMouseClicked

