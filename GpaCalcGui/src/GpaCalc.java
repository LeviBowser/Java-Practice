    private double CalcClassGPA(double dCredits, String sGrade)
            //this method accepts the parameters for the credits and the grade and calculates
            //the weighted class gpa
            {
                
                
                double dGPA = 0; //initialize variable now to avoid the stupid yellow line
                
                switch(sGrade) //in this case a switch statement seemed for effective and easier to read
                {
                    case "A":
                      dGPA = 4 * dCredits; //i just multiplied the credits straight to the gpa.
                        break;
                    case "A-":
                      dGPA = 3.7 * dCredits; //thanks rodney for the help on this and avoiding 2 extra variables
                        break;
                    case "B+":
                      dGPA = 3.4 * dCredits;
                        break;
                    case "B":
                      dGPA = 3 * dCredits;
                        break;
                    case "B-":
                      dGPA = 2.7 * dCredits; // i included the minus sign in the combo boxes and its recognized them. pleasant suprise to be honest.
                        break;
                    case "C+":
                      dGPA = 2.4 * dCredits;
                        break;
                    case "C":
                      dGPA = 2 * dCredits;
                        break;
                    case "C-":
                      dGPA = 1.7 * dCredits;
                        break;
                    case "D+":
                      dGPA = 1.4 * dCredits;
                        break;
                    case "D":
                      dGPA = 1 * dCredits;
                        break;
                    case "D-":
                      dGPA = .7 * dCredits;
                        break;
                    case "E":
                      dGPA = 0 * dCredits; //i didnt set a default becuase no matter what, one of these was going to be fulfilled.
                        break;
                }
                
                return dGPA; //the resulting gpa is the number given back to the object that called it.
               
            }
